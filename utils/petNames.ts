export function getRandomPetName(gender?: string): string {
  const g = gender?.toLowerCase() ?? ''
  
  if (g === 'macho') {
    return MALE_PET_NAMES[Math.floor(Math.random() * MALE_PET_NAMES.length)]!
  }
  
  if (g === 'hembra') {
    return FEMALE_PET_NAMES[Math.floor(Math.random() * FEMALE_PET_NAMES.length)]!
  }
  
  // Si no hay género o es un valor diferente, combinar ambos listados
  const allNames = [...MALE_PET_NAMES, ...FEMALE_PET_NAMES]
  return allNames[Math.floor(Math.random() * allNames.length)]!
}